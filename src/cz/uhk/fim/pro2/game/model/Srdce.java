package cz.uhk.fim.pro2.game.model;

public class Srdce {

	private float pozX, pozY;

	public Srdce(float pozX,float pozY) {
		this.pozX = pozX;
		this.pozY = pozY;
	}

	public float getPozX() {
		return pozX;
	}

	public void setPozX(float pozX) {
		this.pozX = pozX;
	}

	public float getPozY() {
		return pozY;
	}

	public void setPozY(float pozY) {
		this.pozY = pozY;
	}
	
	
	
}
